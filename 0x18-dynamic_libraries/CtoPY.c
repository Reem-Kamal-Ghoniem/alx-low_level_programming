#include "CtoPY.h"
/**
 * add - add to numbers
 * @a: first num
 * @b: second num
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - add to numbers
 * @a: first num
 * @b: second num
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - add to numbers
 * @a: first num
 * @b: second num
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - add to numbers
 * @a: first num
 * @b: second num
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - add to numbers
 * @a: first num
 * @b: second num
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
